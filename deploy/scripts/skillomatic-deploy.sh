#!/bin/bash
# ============================================================================
# Skillomatic IT Deployment Script
# ============================================================================
# Deploys Skillomatic client components to recruiter workstations.
# Run with sudo for system-wide installation.
#
# Usage:
#   sudo ./skillomatic-deploy.sh --api-url https://skillomatic.company.com
#   sudo ./skillomatic-deploy.sh --api-url https://skillomatic.company.com --user jsmith
#
# Options:
#   --api-url URL       Skillomatic API URL (required)
#   --user USERNAME     Deploy for specific user (default: console user)
#   --help              Show this help message
#
# ============================================================================

set -e

VERSION="2.0.0"

# ============================================================================
# Configuration
# ============================================================================

SKILLOMATIC_API_URL=""
USER_HOME=""
CURRENT_USER=""
VERBOSE="false"

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

# ============================================================================
# Helper Functions
# ============================================================================

log_info() {
  echo -e "${GREEN}[INFO]${NC} $1"
}

log_warn() {
  echo -e "${YELLOW}[WARN]${NC} $1"
}

log_error() {
  echo -e "${RED}[ERROR]${NC} $1"
}

show_help() {
  head -20 "$0" | tail -15
  exit 0
}

check_root() {
  if [ "$EUID" -ne 0 ]; then
    log_error "This script must be run with sudo"
    exit 1
  fi
}

detect_os() {
  case "$(uname -s)" in
    Darwin*)  echo "macos" ;;
    Linux*)   echo "linux" ;;
    MINGW*|MSYS*|CYGWIN*) echo "windows" ;;
    *)        echo "unknown" ;;
  esac
}

# ============================================================================
# Parse Arguments
# ============================================================================

while [[ $# -gt 0 ]]; do
  case $1 in
    --api-url)
      SKILLOMATIC_API_URL="$2"
      shift 2
      ;;
    --user)
      CURRENT_USER="$2"
      shift 2
      ;;
    --verbose|-v)
      VERBOSE="true"
      shift
      ;;
    --help|-h)
      show_help
      ;;
    *)
      log_error "Unknown option: $1"
      echo "Use --help for usage information"
      exit 1
      ;;
  esac
done

# ============================================================================
# Validation
# ============================================================================

if [ -z "$SKILLOMATIC_API_URL" ]; then
  log_error "--api-url is required"
  echo "Example: sudo ./skillomatic-deploy.sh --api-url https://skillomatic.company.com"
  exit 1
fi

OS=$(detect_os)
if [ "$OS" = "unknown" ]; then
  log_error "Unsupported operating system"
  exit 1
fi

# Detect current user
if [ -z "$CURRENT_USER" ]; then
  if [ "$OS" = "macos" ]; then
    CURRENT_USER=$(stat -f "%Su" /dev/console 2>/dev/null || echo "$SUDO_USER")
  else
    CURRENT_USER="$SUDO_USER"
  fi
fi

if [ -z "$CURRENT_USER" ] || [ "$CURRENT_USER" = "root" ]; then
  log_error "Could not detect user. Use --user to specify."
  exit 1
fi

USER_HOME=$(eval echo "~$CURRENT_USER")

# ============================================================================
# Display Configuration
# ============================================================================

echo ""
echo "============================================"
echo "  Skillomatic IT Deployment Script v$VERSION"
echo "============================================"
echo ""
echo "  Configuration:"
echo "    API URL:      $SKILLOMATIC_API_URL"
echo "    User:         $CURRENT_USER"
echo "    Home:         $USER_HOME"
echo "    OS:           $OS"
echo ""
echo "============================================"
echo ""

# ============================================================================
# Step 1: Create Directories
# ============================================================================

log_info "[1/4] Creating directories..."

sudo -u "$CURRENT_USER" mkdir -p "$USER_HOME/.skillomatic"
sudo -u "$CURRENT_USER" mkdir -p "$USER_HOME/.claude/commands"

# ============================================================================
# Step 2: Create Skillomatic Configuration
# ============================================================================

log_info "[2/4] Creating Skillomatic configuration..."

# Create config file
cat > "$USER_HOME/.skillomatic/config.json" << EOF
{
  "apiUrl": "$SKILLOMATIC_API_URL",
  "installed": "$(date -u +%Y-%m-%dT%H:%M:%SZ)",
  "version": "$VERSION"
}
EOF
chown "$CURRENT_USER" "$USER_HOME/.skillomatic/config.json"

# Create shell initialization script
cat > "$USER_HOME/.skillomatic/shell-init.sh" << EOF
# Skillomatic environment configuration
# Generated by skillomatic-deploy.sh on $(date)

export SKILLOMATIC_API_URL="$SKILLOMATIC_API_URL"

# Load API key from Keychain (macOS) or secret-tool (Linux)
if [ "\$(uname)" = "Darwin" ]; then
  _skillomatic_key=\$(security find-generic-password -a "\$USER" -s "SKILLOMATIC_API_KEY" -w 2>/dev/null)
elif command -v secret-tool &> /dev/null; then
  _skillomatic_key=\$(secret-tool lookup service skillomatic username "\$USER" 2>/dev/null)
fi

if [ -n "\$_skillomatic_key" ]; then
  export SKILLOMATIC_API_KEY="\$_skillomatic_key"
fi
unset _skillomatic_key
EOF
chown "$CURRENT_USER" "$USER_HOME/.skillomatic/shell-init.sh"

# Add to shell profiles
for profile in "$USER_HOME/.zshrc" "$USER_HOME/.bashrc" "$USER_HOME/.bash_profile"; do
  if [ -f "$profile" ] || [ "$profile" = "$USER_HOME/.zshrc" ]; then
    if ! grep -q "skillomatic/shell-init.sh" "$profile" 2>/dev/null; then
      echo "" >> "$profile"
      echo "# Skillomatic" >> "$profile"
      echo "[ -f ~/.skillomatic/shell-init.sh ] && source ~/.skillomatic/shell-init.sh" >> "$profile"
      chown "$CURRENT_USER" "$profile"
    fi
  fi
done

# ============================================================================
# Step 3: Install Health Check Skill
# ============================================================================

log_info "[3/4] Installing health check skill..."

SKILLS_DIR="$USER_HOME/.claude/commands"

# Create health check skill
cat > "$SKILLS_DIR/skillomatic-health-check.md" << 'SKILLEOF'
---
name: skillomatic-health-check
description: Verify your Skillomatic installation is working correctly
intent: I want to check if Skillomatic is set up correctly
capabilities:
  - Check API key configuration
  - Test API connectivity
allowed-tools:
  - Bash
  - Read
---

# Skillomatic Health Check

Run these checks to verify your installation:

## 1. Environment Check

```bash
echo "=== Skillomatic Health Check ==="
echo ""

# Check API URL
if [ -n "$SKILLOMATIC_API_URL" ]; then
  echo "✓ API URL: $SKILLOMATIC_API_URL"
else
  echo "✗ SKILLOMATIC_API_URL not set"
fi

# Check API key
if [ -n "$SKILLOMATIC_API_KEY" ]; then
  echo "✓ API key is configured (${SKILLOMATIC_API_KEY:0:10}...)"
else
  echo "✗ SKILLOMATIC_API_KEY not set"
  echo "  To fix: security add-generic-password -a $USER -s SKILLOMATIC_API_KEY -w 'your-key'"
fi
```

## 2. API Connectivity

```bash
if [ -n "$SKILLOMATIC_API_KEY" ]; then
  response=$(curl -s -w "\n%{http_code}" \
    -H "Authorization: Bearer $SKILLOMATIC_API_KEY" \
    "$SKILLOMATIC_API_URL/api/v1/me")

  http_code=$(echo "$response" | tail -1)
  body=$(echo "$response" | head -n -1)

  if [ "$http_code" = "200" ]; then
    echo "✓ API connection successful"
    echo "  User: $(echo $body | grep -o '"email":"[^"]*"' | cut -d'"' -f4)"
  else
    echo "✗ API connection failed (HTTP $http_code)"
  fi
else
  echo "⊘ Skipping API check (no API key)"
fi
```

## 3. Skills Check

```bash
echo ""
echo "=== Installed Skills ==="
ls -la ~/.claude/commands/
```

## Summary

If any checks failed, contact IT support with the output above.
SKILLEOF

chown -R "$CURRENT_USER" "$SKILLS_DIR"

# ============================================================================
# Step 4: Summary
# ============================================================================

log_info "[4/4] Deployment complete!"

echo ""
echo "============================================"
echo "  Deployment Complete!"
echo "============================================"
echo ""
echo "  Installed components:"
echo "    ✓ Skillomatic config: ~/.skillomatic/"
echo "    ✓ Shell initialization: ~/.skillomatic/shell-init.sh"
echo "    ✓ Health check skill: ~/.claude/commands/"
echo ""
echo "  Next steps for the user:"
echo "    1. Log in to $SKILLOMATIC_API_URL"
echo "    2. Generate an API key from the dashboard"
echo "    3. Store it securely:"

if [ "$OS" = "macos" ]; then
  echo "       security add-generic-password -a \$USER -s SKILLOMATIC_API_KEY -w 'sk_live_xxx'"
elif [ "$OS" = "linux" ]; then
  echo "       secret-tool store --label='Skillomatic API Key' service skillomatic username \$USER"
fi

echo "    4. Download skills from the Skillomatic web UI"
echo "    5. Move skills to ~/.claude/commands/"
echo "    6. Restart terminal"
echo ""
echo "  Verify installation:"
echo "    Type /skillomatic-health-check in Claude"
echo ""
echo "============================================"
